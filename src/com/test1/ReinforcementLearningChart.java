package com.test1;

public class ReinforcementLearningChart {
	private Integer dataLearningCode;
	private Integer reinforcementLearningId;
	private String reinforcementLearningBudgetIndicator;
	private String reinforcementLearningSheet;
	private String reinforcementLearningControlFlag;
	private String reinforcementLearningDesc;
	private String refinforcementLearningShortDesc;
	private String reinforcementLearningType;
	private String creationDate;
	private String modifiedDate;
	private String entityState;

	public ReinforcementLearningChart() {

	}

	public ReinforcementLearningChart(Integer dataLearningCode, Integer reinforcementLearningId,
			String reinforcementLearningBudgetIndicator, String reinforcementLearningSheet,
			String reinforcementLearningControlFlag, String reinforcementLearningDesc,
			String refinforcementLearningShortDesc, String reinforcementLearningType, String creationDate,
			String modifiedDate, String entityState) {
		super();
		this.dataLearningCode = dataLearningCode;
		this.reinforcementLearningId = reinforcementLearningId;
		this.reinforcementLearningBudgetIndicator = reinforcementLearningBudgetIndicator;
		this.reinforcementLearningSheet = reinforcementLearningSheet;
		this.reinforcementLearningControlFlag = reinforcementLearningControlFlag;
		this.reinforcementLearningDesc = reinforcementLearningDesc;
		this.refinforcementLearningShortDesc = refinforcementLearningShortDesc;
		this.reinforcementLearningType = reinforcementLearningType;
		this.creationDate = creationDate;
		this.modifiedDate = modifiedDate;
		this.entityState = entityState;
	}

	public Integer getDataLearningCode() {
		return dataLearningCode;
	}

	public void setDataLearningCode(Integer dataLearningCode) {
		this.dataLearningCode = dataLearningCode;
	}

	public Integer getReinforcementLearningId() {
		return reinforcementLearningId;
	}

	public void setReinforcementLearningId(Integer reinforcementLearningId) {
		this.reinforcementLearningId = reinforcementLearningId;
	}

	public String getReinforcementLearningBudgetIndicator() {
		return reinforcementLearningBudgetIndicator;
	}

	public void setReinforcementLearningBudgetIndicator(String reinforcementLearningBudgetIndicator) {
		this.reinforcementLearningBudgetIndicator = reinforcementLearningBudgetIndicator;
	}

	public String getReinforcementLearningSheet() {
		return reinforcementLearningSheet;
	}

	public void setReinforcementLearningSheet(String reinforcementLearningSheet) {
		this.reinforcementLearningSheet = reinforcementLearningSheet;
	}

	public String getReinforcementLearningControlFlag() {
		return reinforcementLearningControlFlag;
	}

	public void setReinforcementLearningControlFlag(String reinforcementLearningControlFlag) {
		this.reinforcementLearningControlFlag = reinforcementLearningControlFlag;
	}

	public String getReinforcementLearningDesc() {
		return reinforcementLearningDesc;
	}

	public void setReinforcementLearningDesc(String reinforcementLearningDesc) {
		this.reinforcementLearningDesc = reinforcementLearningDesc;
	}

	public String getRefinforcementLearningShortDesc() {
		return refinforcementLearningShortDesc;
	}

	public void setRefinforcementLearningShortDesc(String refinforcementLearningShortDesc) {
		this.refinforcementLearningShortDesc = refinforcementLearningShortDesc;
	}

	public String getReinforcementLearningType() {
		return reinforcementLearningType;
	}

	public void setReinforcementLearningType(String reinforcementLearningType) {
		this.reinforcementLearningType = reinforcementLearningType;
	}

	public String getCreationDate() {
		return creationDate;
	}

	public void setCreationDate(String creationDate) {
		this.creationDate = creationDate;
	}

	public String getModifiedDate() {
		return modifiedDate;
	}

	public void setModifiedDate(String modifiedDate) {
		this.modifiedDate = modifiedDate;
	}

	public String getEntityState() {
		return entityState;
	}

	public void setEntityState(String entityState) {
		this.entityState = entityState;
	}

}
